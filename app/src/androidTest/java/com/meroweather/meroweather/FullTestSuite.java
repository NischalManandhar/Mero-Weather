package com.meroweather.meroweather;

import android.test.suitebuilder.TestSuiteBuilder;

import junit.framework.Test;
import junit.framework.TestSuite;

public class FullTestSuite extends TestSuite {
    //1. This FullTestSuite class contains the code to include all of the java test classes
    //   in its package into a suite of tests the JUnit will run
    //2. This allows us to easily add additional tests,by just adding additional Java class
    //   files to our test directory.
    //3. This file is not project specific so we can literally copy to another project.
    public FullTestSuite() {
        super();
    }

    public static Test suite() {
        return new TestSuiteBuilder(FullTestSuite.class)
                .includeAllPackagesUnderHere().build();
    }
}
